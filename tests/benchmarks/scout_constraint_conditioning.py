"""
Scout: Constraint Conditioning Benchmark
========================================

Measures the numerical conditioning of the Linear Constraints generated by CBFs/CLFs.
High condition numbers (LgB matrix) indicate potential solver failures or instability.

Metrics:
- Condition Number of the Constraint Matrix (LgB)
- Constraint Generation Time vs Total Step Time
"""

import sys
import os
import time
import jax
import jax.numpy as jnp
import numpy as np

# Ensure src is in path
sys.path.append(os.path.abspath(os.path.join(os.path.dirname(__file__), "../../src")))

from cbfkit.systems.unicycle.models import accel_unicycle
from cbfkit.certificates import rectify_relative_degree, concatenate_certificates
from cbfkit.certificates.conditions.barrier_conditions import zeroing_barriers
from cbfkit.controllers.cbf_clf.generate_constraints.generating_functions import (
    generate_compute_cbf_clf_constraints,
    generate_compute_input_constraints,
)
from cbfkit.controllers.cbf_clf.generate_constraints.zeroing_cbfs import (
    generate_compute_zeroing_cbf_constraints,
)
from cbfkit.controllers.cbf_clf.generate_constraints.vanilla_clfs import (
    generate_compute_vanilla_clf_constraints,
)

def ellipsoid_cbf(obstacle, ellipsoid):
    center = obstacle[:2]
    radii = ellipsoid
    P = jnp.diag(1.0 / (radii**2))
    def h(x_and_t):
        pos = x_and_t[:2]
        diff = pos - center
        return jnp.dot(diff, jnp.dot(P, diff)) - 1.0
    return h

def run():
    print("🔎 Scout: Starting Constraint Conditioning Benchmark...")

    # 1. Setup System
    dyn = accel_unicycle.plant(lam=1.0)
    control_limits = jnp.array([10.0, 10.0])

    # 2. Setup Barrier (Unicycle approaching obstacle)
    obs = jnp.array([2.0, 0.0, 0.0])
    ell = jnp.array([0.5, 0.5])

    barrier_pkg = rectify_relative_degree(
        function=ellipsoid_cbf(obs, ell),
        system_dynamics=dyn,
        state_dim=4,
        form="exponential",
    )(
        certificate_conditions=zeroing_barriers.linear_class_k(1.0),
        obstacle=obs,
        ellipsoid=ell,
    )

    barriers = concatenate_certificates(barrier_pkg)

    # 3. Generate Constraint Function
    # We use the generators directly to isolate constraint computation

    # IMPORTANT: When relaxable_cbf=True, we must augment control_limits manually
    # to account for slack variables, matching cbf_clf_qp_generator logic.
    n_bfs = 1
    slack_bound = 1e4
    control_limits_aug = jnp.hstack([control_limits, slack_bound * jnp.ones((n_bfs,))])

    compute_cbf_clf = generate_compute_cbf_clf_constraints(
        generate_compute_cbf_constraints=generate_compute_zeroing_cbf_constraints,
        generate_compute_clf_constraints=generate_compute_vanilla_clf_constraints,
        control_limits=control_limits_aug,
        dyn_func=dyn,
        barriers=barriers,
        relaxable_cbf=True, # Typically true in real apps
    )

    compute_input = generate_compute_input_constraints(control_limits_aug)

    # JIT compile the constraint generator for fairness
    compute_cbf_clf_jit = jax.jit(compute_cbf_clf)
    compute_input_jit = jax.jit(compute_input)

    # Warmup
    x0 = jnp.array([0.0, 0.0, 0.0, 1.0])
    print("  Compiling functions...")
    compute_cbf_clf_jit(0.0, x0)
    compute_input_jit(0.0, x0)

    # 4. Simulation Loop (Fake trajectory moving towards obstacle)
    steps = 100
    cond_nums = []
    times = []

    # Move x from (0,0) to (2,0)
    xs = np.linspace(0, 1.9, steps) # Don't go inside obstacle

    print(f"  Profiling {steps} steps...")

    for i in range(steps):
        # State: x, y, v, theta
        # Head on collision course
        x = jnp.array([xs[i], 0.0, 1.0, 0.0])

        t0 = time.time()

        # Compute Input Constraints
        Au, bu = compute_input_jit(0.0, x)

        # Compute CBF Constraints
        Ac, bc, _ = compute_cbf_clf_jit(0.0, x)

        # Stack
        A = jnp.vstack([Au, Ac])

        # Force block_until_ready for timing
        A.block_until_ready()
        t1 = time.time()

        times.append((t1 - t0) * 1e3) # ms

        # Compute Condition Number
        # SVD is robust
        try:
            cond = jnp.linalg.cond(A)
            cond_nums.append(float(cond))
        except Exception as e:
            print(f"SVD Failed at step {i}: {e}")
            cond_nums.append(float('nan'))

    # 5. Report
    cond_nums = np.array(cond_nums)
    times = np.array(times)

    print("\n📊 Constraint Conditioning Stats:")
    print(f"  Mean Condition #: {np.nanmean(cond_nums):.2e}")
    print(f"  Max Condition #:  {np.nanmax(cond_nums):.2e}")
    print(f"  Steps > 1e4:      {np.sum(cond_nums > 1e4)}")
    print(f"  Steps > 1e6:      {np.sum(cond_nums > 1e6)} (Likely unstable)")

    print("\n⏱️ Timing (Constraint Gen):")
    print(f"  Mean Time: {np.mean(times):.4f} ms")
    print(f"  Max Time:  {np.max(times):.4f} ms")

if __name__ == "__main__":
    run()
